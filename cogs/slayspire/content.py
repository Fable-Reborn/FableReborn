@dataclass(frozen=True)
class CardDef:
    key: str
    name: str
    card_type: str
    rarity: str
    cost: int
    target: str
    description: str
    actions: list[dict[str, Any]]
    upgraded_description: str | None = None
    upgraded_actions: list[dict[str, Any]] | None = None
    exhaust: bool = False
    upgraded_exhaust: bool | None = None
    playable: bool = True
    upgraded_cost: int | None = None
    retain: bool = False
    upgraded_retain: bool | None = None
    ethereal: bool = False
    innate: bool = False
    upgraded_innate: bool | None = None


